/* **************************** [^] INCLUDES [^] **************************** */

float
	ft_eluf(register float x, float alpha)
{
	if (x > 0.0F)
	{
		alpha = x;
		return (alpha);
	}
	alpha *= (ft_expf(x) - 1.0F);
	return (alpha);
}
