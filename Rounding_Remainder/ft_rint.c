/* INCLUDES */
#include "../ft_math.h"
/* INCLUDES */

double
	ft_rint(register double x)
{
	if (x > 0.0)
		return (ft_floor(x + 0.5));
	if (x < 0.0)
		return (ft_ceil(x - 0.5));
	return (x);
}
