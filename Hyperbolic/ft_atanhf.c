/* **************************** [^] INCLUDES [^] **************************** */

float
	ft_atanhf(register float x)
{
	if (!(ft_fabsf(x) >= 1.0F))
		return (0.5F * ft_logf((1.0F + x) / (1.0F - x)));
	if (x == 1.0F)
		return (1.0F / 0.0F);
	if (x == -1.0F)
		return (-1.0F / 0.0F);
	return (0.0F / 0.0F);
}
