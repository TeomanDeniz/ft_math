#include "../ft_math.h"
/* INCLUDES */

double
	ft_log2(register double x)
{
	if (ft_isnan(x) || ft_isinf(x))
		return (x);
	if (x == 0.0)
		return ((-1.0 / 0.0));
	if (x < 0.0)
		return ((0.0 / 0.0));
	return (ft_log(x) / M_LN2);
}
