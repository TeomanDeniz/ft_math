#include	"../ft_math.h"

double
	ft_invsqrt(register double x)
{
	double		y;
	long long	i;
	i = *(long long *)&y;
	y = x;
	i = 0X5FE6EB50C7B537A9 - (i >> 1);
	y = *(double *)&i;
	return (y * (1.5 - 0.5 * x * y * y));
}
