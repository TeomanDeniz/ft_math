double
	ft_atan(register double x)
{
	register double	y;

	if (ft_isnan(x))
		return (x);
	if (x == 0.0)
		return (0.0);
	if (x > 0.0)
	{
		if (x > 1.0)
			return (M_PI / 2 - ft_atan(1.0 / x));
		y = (x * x);
		return (x * (1.0 - \
			y * (0.33333333333333333 - y * (0.20000000000000000 - \
			y * (0.14285714285714285 - y * (0.11111111111111111 - \
			y * (0.09090909090909091 - y * (0.07692307692307693) \
			)))))));
	}
	return (-ft_atan(-x));
}
